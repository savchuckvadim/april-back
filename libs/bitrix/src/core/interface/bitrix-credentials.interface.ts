/**
 * Минимальный набор учётных данных, которого достаточно
 * клиенту Bitrix API для работы с конкретным порталом.
 *
 * Библиотека НЕ знает про доменную модель портала (IPortal),
 * про БД токенов и про то, как обновляется OAuth-токен —
 * она получает уже готовые credentials извне (через PBX).
 */
export interface BitrixCredentials {
    /** Домен портала, например example.bitrix24.ru */
    domain: string;
    /** Ключ вебхука (для авторизации по протоколу HOOK) */
    key?: string;
    /** Access-токен (для авторизации по протоколу TOKEN) */
    accessToken?: string;
}
